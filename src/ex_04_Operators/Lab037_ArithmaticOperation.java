package ex_04_Operators;

public class Lab037_ArithmaticOperation {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        float c = 3.14f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(a/c);
        System.out.println(b%a);
    }
}
