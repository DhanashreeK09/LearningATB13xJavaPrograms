package ex_04_Operators;

public class Lab040_RelationalOperators {
    public static void main(String[] args) {
        /*
        < Less than
        > Greater than
        <= Less than Equal to
        >= Greater than Equal to
        == Equal to
        != Not equal to

        All of them will result in boolean output
         */

        int a = 10;
        int b = 20;
        boolean c = a>b;
        System.out.println(c);
    }
}
