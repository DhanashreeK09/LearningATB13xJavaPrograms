package ex_04_Operators;

public class Lab041_LogicalOperators {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a);
        System.out.println(!!a);
        boolean c = true || false;
        boolean c1 = true && false;
        System.out.println(c);

        System.out.println(c1);

    }
}
